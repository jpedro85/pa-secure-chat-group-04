package Utils.Message.EnumTypes;

/**
 * Represents all the types a message can have.
 */
public enum ContentTypes
{
    /** Error content type. */
    ERROR,
    /** Account-related content type. */
    ACCOUNT,
    /** Certificate authority communication content type. */
    CA_COMMUNICATION,
    /** Diffie-Hellman key exchange content type. */
    DIFFIE_HELLMAN,
    /** General communication content type. */
    COMMUNICATION
}
